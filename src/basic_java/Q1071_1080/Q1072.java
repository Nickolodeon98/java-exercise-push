package basic_java.Q1071_1080;

import java.util.Scanner;

public class Q1072 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int num;
        num = Integer.parseInt(sc.nextLine());
        int a;
        for (int i = 0; i < num; i++) {
            a = sc.nextInt();
            System.out.println(a);
        }
    }
}
